# 문자열은 쌍따옴표 또는 홑따옴표로 묶어서 표현
ex1 = "Hello World!"
ex2 = 'Hello World!'
ex3 = "My favorite food is cheese"

# 쌍따옴표 또는 홑따옴표를 그대로 보여주고 싶을 때는 기호 앞에 \를 붙여서 사용
ex4 = '"Python is very easy." he says.'
ex5 = 'Python\'s favorite food is perl'
ex6 = "\"Python is very easy.\" he says."

# 줄바꿈 할 때는 \n 사용
ex7 = "Life is too short\nYou need python"

print(ex1)
print(ex2)
print(ex3)
print(ex4)
print(ex5)
print(ex6)
print(ex7)